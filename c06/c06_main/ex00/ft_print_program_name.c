#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	program(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	program(argv[0]);
	ft_putchar('\n');
}
