#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	params_rev(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int stop;

	stop = argc - 1;
	while (stop > 0)
	{
		params_rev(argv[stop]);
		ft_putchar('\n');
		stop--;
	}
}
