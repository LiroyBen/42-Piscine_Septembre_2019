#include <stdio.h>

int	ft_sqrt(int nb)
{
	int n;

	n = 1;
	if (nb == 0)
	{
		return (0);
	}
	while ((n * n) < nb)
	{
		n++;
	}
	if ((n*n) == nb)
		return (n);
	else
		return (0);
}

int main()
{
	printf("%d", ft_sqrt(0));
}
