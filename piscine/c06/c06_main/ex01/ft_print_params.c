#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	params(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int stop;

	stop = 1;
	while (stop < argc)
	{
		params(argv[stop]);
		ft_putchar('\n');
		stop++;
	}
	return (0);
}
